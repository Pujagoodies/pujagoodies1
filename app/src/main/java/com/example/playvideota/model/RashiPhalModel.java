package com.example.playvideota.model;

public class RashiPhalModel {
    int RashiphalImage;
    String RashiphalName;

    public RashiPhalModel(int rashiphalImage, String rashiphalName) {
        RashiphalImage = rashiphalImage;
        RashiphalName = rashiphalName;
    }

    public int getRashiphalImage() {
        return RashiphalImage;
    }

    public void setRashiphalImage(int rashiphalImage) {
        RashiphalImage = rashiphalImage;
    }

    public String getRashiphalName() {
        return RashiphalName;
    }

    public void setRashiphalName(String rashiphalName) {
        RashiphalName = rashiphalName;
    }
}
